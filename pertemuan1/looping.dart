void main() {
  //while-lopp

  var flag = 1;
  while (flag < 10 ) {
    //jika dikasi = akan menjadi sampai 10
    print("literasi ke - " + flag.toString());
    flag++;
  }

  // for-loop
  print("---------FOR-LOOP---------");

  for(var angka = 1; angka <10; angka++) {
    print("literasi ke - " + angka.toString());
  }
}